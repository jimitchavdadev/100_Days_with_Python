# Band Name Generator

# Print a welcome message to the user
print("Welcome to the Band Name Generator.")

# Prompt the user to input the name of the city they grew up in and store the response in the variable city_name
city_name = input("What's the name of the city you grew up in?\n")

# Prompt the user to input their pet's name and store the response in the variable pet_name
pet_name = input("What's your pet's name?\n")

# Generate and print the band name by combining the city_name and pet_name
print(f"Your band name can be {city_name} {pet_name}")
